
public class SimEnkaInputReaderException extends Exception {

	private static final long serialVersionUID = -2695877383196573975L;

	public SimEnkaInputReaderException() {
		super();
	}

	public SimEnkaInputReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public SimEnkaInputReaderException(String message) {
		super(message);
	}

	public SimEnkaInputReaderException(Throwable cause) {
		super(cause);
	}

	
}
